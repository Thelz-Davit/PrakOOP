package Model;

/**
 *
 * @author Davit
 */
public class Costumer {
    private String costumer_id;
    private String nama_costumer;
    private String alamat;
    private String email;
    private String notlp;

    public String getCostumer_id() {
        return costumer_id;
    }

    public void setCostumer_id(String costumer_id) {
        this.costumer_id = costumer_id;
    }

    public String getNama_costumer() {
        return nama_costumer;
    }

    public void setNama_costumer(String nama_costumer) {
        this.nama_costumer = nama_costumer;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNotlp() {
        return notlp;
    }

    public void setNotlp(String notlp) {
        this.notlp = notlp;
    }
}
