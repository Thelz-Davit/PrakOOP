    public final void getData(){
        DefaultTableModel dtm = (DefaultTableModel) tblBaju.getModel();
        
        dtm.setRowCount(0);
        List<Baju> listBaju = conBaju.showBaju();
        String[] data = new String[7];
        for (Baju newBaju : listBaju) {
            data[0] = newBaju.getId_baju();
            data[1] = newBaju.getNama_baju();
            data[2] = newBaju.getKategori();
            data[3] = newBaju.getKetersediaan();
            data[4] = newBaju.getKerusakan();
            data[5] = newBaju.getKelengkapan();
            data[6] = Double.toString(newBaju.getHarga());
            dtm.addRow(data);
        }
    }
    
