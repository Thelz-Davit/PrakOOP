    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String ketersediaan = "";
        if (rbYes.isSelected()) {
            ketersediaan = "Yes";
        }
        else if (rbNo.isSelected()) {
            ketersediaan = "No";
        }
        int i = tblBaju.getSelectedRow();
        if (i==-1) {
            JOptionPane.showMessageDialog(btnUpdate, "Harap Pilih Satu Data","Warning!",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        conBaju.updateBaju(txtid.getText(), txtBaju.getText(), (String) cmbKategori.getSelectedItem(), ketersediaan, (String) cmbKerusakan.getSelectedItem(),(String) cmbKelengkapan.getSelectedItem(), Double.parseDouble(txtHarga.getText()) );
        JOptionPane.showMessageDialog(btnUpdate, "Data berhasil diupdate");
        getData();
    }//GEN-LAST:event_btnUpdateActionPerformed

