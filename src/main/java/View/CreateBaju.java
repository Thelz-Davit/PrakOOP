    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int i = tblBaju.getSelectedRow();
        if (i==-1) {
            JOptionPane.showMessageDialog(btnDelete, "Harap Pilih satu data","Warning!",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        conBaju.deleteBaju(txtid.getText());
        JOptionPane.showMessageDialog(btnDelete, "Data Berhasil dihapus");
        getData();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String ketersediaan = "";
        if (rbYes.isSelected()) {
            ketersediaan = "Yes";
        }
        else if (rbNo.isSelected()) {
            ketersediaan = "No";
        }
        hasil = conBaju.insertBaju(txtid.getText(), txtBaju.getText(), (String) cmbKategori.getSelectedItem(), ketersediaan, (String) cmbKerusakan.getSelectedItem(),(String) cmbKelengkapan.getSelectedItem(), Double.parseDouble(txtHarga.getText()));
        if (hasil) {
            JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan");
            getData();
            clearData();
        }
        else{
            JOptionPane.showMessageDialog(null, "Data gagal Ditambahkan", " Pesan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void tblBajuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBajuMouseClicked
        // TODO add your handling code here:
        txtid.setEditable(false);
        int i = tblBaju.getSelectedRow();
        
        TableModel model = tblBaju.getModel();
        txtid.setText(model.getValueAt(i, 0).toString());
        txtBaju.setText(model.getValueAt(i, 1).toString());
        cmbKategori.setSelectedItem(model.getValueAt(i, 2));
        String ketersediaan = (model.getValueAt(i, 3).toString());
        if(ketersediaan.equals("Yes")){
            rbYes.setSelected(true);
        }else if (ketersediaan.equals("No")){
            rbNo.setSelected(true);   
        }
    }//GEN-LAST:event_tblBajuMouseClicked

