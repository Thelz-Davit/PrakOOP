    public final void getData(){
        DefaultTableModel dtm = (DefaultTableModel) tblCostumer.getModel();
        
        dtm.setRowCount(0);
        List<Costumer> listCostumer = conCos.showCostumer();
        String[] data = new String[5];
        for (Costumer newCostumer : listCostumer) {
            data[0] = newCostumer.getCostumer_id();
            data[1] = newCostumer.getNama_costumer();
            data[2] = newCostumer.getAlamat();
            data[3] = newCostumer.getEmail();
            data[4] = newCostumer.getNotlp();
            dtm.addRow(data);
        }
    }
    
