    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int i = tblCostumer.getSelectedRow();
        if (i==-1) {
            JOptionPane.showMessageDialog(btnDelete, "Harap Pilih satu data","Warning!",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        conCos.deleteCostumer(txtIdCos.getText());
        JOptionPane.showMessageDialog(btnDelete, "Data Berhasil dihapus");
        getData();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        hasil = conCos.insertCostumer(txtIdCos.getText(), txtCostumer.getText(), txtAlamat.getText(), txtEmail.getText(), txtTlp.getText());
        if (hasil) {
            JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan");
            getData();
            clearData();
        }
        else{
            JOptionPane.showMessageDialog(null, "Data gagal Ditambahkan", " Pesan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void tblCostumerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCostumerMouseClicked
        // TODO add your handling code here:
        txtIdCos.setEditable(false);
        int i = tblCostumer.getSelectedRow();
        
        TableModel model = tblCostumer.getModel();
        txtIdCos.setText(model.getValueAt(i, 0).toString());
        txtCostumer.setText(model.getValueAt(i, 1).toString());
        txtAlamat.setText(model.getValueAt(i, 2).toString());
        txtEmail.setText(model.getValueAt(i, 3).toString());
        txtTlp.setText(model.getValueAt(i, 4).toString());
    }//GEN-LAST:event_tblCostumerMouseClicked

