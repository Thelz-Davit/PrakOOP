    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int i = tblCostumer.getSelectedRow();
        if (i==-1) {
            JOptionPane.showMessageDialog(btnUpdate, "Harap Pilih Satu Data","Warning!",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        conCos.updateCostumer(txtIdCos.getText(), txtCostumer.getText(), txtAlamat.getText(), txtEmail.getText(), txtTlp.getText());
        JOptionPane.showMessageDialog(btnUpdate, "Data berhasil diupdate");
        getData();
    }//GEN-LAST:event_btnUpdateActionPerformed

